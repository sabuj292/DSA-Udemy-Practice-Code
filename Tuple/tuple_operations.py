
# Tuple Operations / Functions

myTuple2 = (1,2,4,7,8,9)
myTuple = (1,2,4,7,8,9)
myTuple1 = (1,2,6,9,8,7)

# concatination using '+'

print(myTuple2 +myTuple1)

